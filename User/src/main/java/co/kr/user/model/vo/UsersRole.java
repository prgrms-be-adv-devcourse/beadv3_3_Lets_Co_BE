package co.kr.user.model.vo;

/**
 * 사용자의 권한(Role)을 정의하는 Enum 클래스입니다.
 * 시스템 내에서 회원의 등급이나 접근 권한을 구분하는 데 사용됩니다.
 */
public enum UsersRole {
    /**
     * 일반 사용자 (구매자)
     * 회원가입 직후의 기본 권한입니다.
     */
    USERS,

    /**
     * 시스템 관리자
     * 회원 관리, 판매자 승인 등 시스템 전반에 대한 제어 권한을 가집니다.
     */
    ADMIN,

    /**
     * 판매자
     * 상품 등록 및 판매 활동을 할 수 있는 권한을 가진 회원입니다.
     */
    SELLER
}